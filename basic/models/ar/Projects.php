namespace app\models\ar;


class Projects extends \app\models\ar\base\Projects
{

    public function beforeSave($insert)
    {
        $this->user = \Yii::$app->user->id;
        return parent::beforeSave($insert); // TODO: Change the autogenerated stub
    }



    public function getEntityForAutocomplete()
    {
        $docIds[] = 0;
        if ($this->documents) {
            foreach ($this->documents as $doc) {
                $docIds[] = $doc->id;
            }
        }
        $itemsToReturn = [];
        $entityToAutocomplite = \app\models\ar\base\SentencesPlusHl::find()->where(['doc_id' => $docIds])->andWhere('(entity IS NOT NULL OR entity_type IS NOT NULL OR manual_date IS NOT NULL)')->select('entity,entity_type,manual_date')->asArray()->all();
        if ($entityToAutocomplite) {
            foreach ($entityToAutocomplite as $items) {
                foreach ($items as $item) {
                    if ($item != null) {
                        $itemsToReturn[] = $item;
                    }
                }
            }
        }
        $itemsToReturn = array_unique($itemsToReturn);
        $string = implode("', '", $itemsToReturn);


        return "['$string']";
    }

    public function getDocumentsId()
    {
        $docIds = Documents::find()->where(['project_id' => $this->id])->select('id')->asArray()->all();
        if (!$docIds) {
            return false;
        }
        $Ids = array_map(function ($n) {
            return $n['id'];
        }, $docIds);

        return $Ids;
    }

    /**
     * @return \yii\db\ActiveQuery
     */
    public function getDocuments()
    {
        return $this->hasMany(Documents::className(), ['project_id' => 'id']);
    }

    public function getNotes()
    {
        $docIds = $this->getDocumentsId();

        if (!$docIds) {
            return false;
        }

        return SentencesPlusHl::find()->orderBy('doc_id')->where(['user' => \Yii::$app->user->id])->where(['doc_id' => $docIds])->andWhere('tag_type = 1')->all();
    }


    public function getEntity()
    {
        $docIds = $this->getDocumentsId();

        if (!$docIds) {
            return false;
        }

        return SentencesPlusHl::find()
            ->select([
                'id',
                'doc_id',
                'tag_type',
                'selection',
                'sent_hl',
                'manual_date',
                'entity',
                'entity_type',
                'note'
            ])
            ->orderBy('doc_id')
            ->where(['user' => \Yii::$app->user->id])
            ->where(['doc_id' => $docIds])
            ->andWhere('entity IS NOT NULL OR entity_type IS NOT NULL')
            ->all();
    }


    public function extraFields()
    {
        return [
            'documents',
            'entity'
        ];
    }


}